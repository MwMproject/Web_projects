// ═══ SPRITE RENDERER ═══
function drawAnimatedSprite(x, y, cls, state, facingAngle) {
  const data = SPRITE_DATA[cls];
  if (!data) return;

  const frames = data.frames[state] || data.frames.idle;
  const palette = data.palette;

  // Animation timing
  const speed = state === "walk" ? 150 : 500; // ms per frame
  const frameIdx = Math.floor(Date.now() / speed) % frames.length;
  const frame = frames[frameIdx];

  const pxSize = 2; // pixel size on screen
  const rows = frame.length;
  const cols = frame[0].length;
  const ox = x - (cols * pxSize) / 2;
  const oy = y - (rows * pxSize) / 2;

  // Flip horizontally if facing left
  const flip = facingAngle > Math.PI / 2 || facingAngle < -Math.PI / 2;

  ctx.save();
  if (flip) {
    ctx.translate(x, y);
    ctx.scale(-1, 1);
    ctx.translate(-x, -y);
  }

  frame.forEach((row, ry) => {
    for (let cx = 0; cx < row.length; cx++) {
      const c = row[cx];
      if (c === "0") continue;
      const color = palette[c];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(ox + cx * pxSize, oy + ry * pxSize, pxSize, pxSize);
    }
  });

  ctx.restore();
}

// ═══ GET SPRITE STATE ═══
function getSpriteState(p) {
  const isMoving = Math.abs(p.dx) > 0.1 || Math.abs(p.dy) > 0.1;
  return isMoving ? "walk" : "idle";
}
